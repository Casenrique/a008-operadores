let numeroUsuario = Number(prompt("Insira um número par."))
console.log(
`Você entrou o número: ${numeroUsuario}
O resto da divisão do seu número por 2 é: ${numeroUsuario % 2}
`)
// SEMPRE que o usuário entra com um número par, o resto da divisão será 0
// SEMPRE que o usuário entra com um número ímpar, o resto da divisão será 1